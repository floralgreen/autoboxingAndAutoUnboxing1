public class Operations {

    public void stampaSommaInt(int num1, int num2) {
        int somma = num1 + num2;
        System.out.println("La somma dei 2 int è: " + somma);
    }
    public void stampaSommaDouble(double num1, double num2) {
        double somma = num1 + num2;
        System.out.println("La somma dei 2 int è: " + somma);
    }
    public void stampaCaratterePrimitive(char carattere) {
        System.out.println("Il carattere è: '" + carattere + "'");
    }
    public void stampaSommaInteger(Integer num1, Integer num2){
        Integer somma = num1 + num2;
        System.out.println("La somma dei 2 Integer è: " + somma);
    }
    public void stampaSommaDoubleWrapper(Double num1, Double num2) {
        Double somma = num1 + num2;
        System.out.println("La somma dei 2 int è: " + somma);
    }
    public void stampaCarattereWrapper(Character carattere) {
        System.out.println("Il carattere è: '" + carattere + "'");
    }

}
